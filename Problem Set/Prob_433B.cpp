#include<bits/stdc++.h>
#define ll long long
using namespace std;

void cin_array(ll a[], ll n){
    for(int i=0; i<n; i++){
        cin>>a[i];
    }
}

int main(){
    ll n;
    cin >> n;
    ll v[n];
    cin_array(v, n);
    ll m;
    cin >> m;
    while(m--){
        ll type, l, r;
        cin >> type >> l >> r;
    }
    return 0;
}